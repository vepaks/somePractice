function getNum(input) {
    //създаваме променлива. която е брояч със стойност '1'
    let index = 1;
    //създаваме променлива, която ще съхранява в себе стойност от масива
    let inputNum = input[0];

    //правим цикъл, който да печата резултата докато брояча не достигне стойнот от масива.
    while (inputNum >= index) {

        console.log(index);
        index = (index * 2) + 1;

    }

}

